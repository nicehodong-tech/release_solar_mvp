def premium_profile_category_contract(
    domain: Any,
    *,
    feature_axes: list[dict[str, Any]] | None = None,
    risk_score: int | None = None,
) -> dict[str, Any]:
    """Return a dynamic premium section contract calculated in the engine layer.

    Static contracts define what a paid section must cover. This function adds
    chart-specific values, scores, tone, and evidence to the reading units so
    the web UI does not have to infer premium content quality from card text.
    """

    normalized = normalize_premium_topic_domain(domain)
    contract = premium_category_contract(normalized)
    if not contract:
        return {}
    topics = premium_topic_items(
        normalized,
        limit=_profile_topic_limit(normalized, contract),
        feature_axes=feature_axes,
    )
    if normalized == "money":
        topics = _aligned_money_topic_items(topics, risk_score=risk_score)
    if normalized == "career":
        topics = _aligned_career_topic_items(topics, risk_score=risk_score)
    lookup = _topic_lookup(topics)
    reading_units: list[dict[str, Any]] = []
    for raw_unit in contract.get("reading_units") or ():
        if not isinstance(raw_unit, dict):
            continue
        label = str(raw_unit.get("label") or "").strip()
        topic_title = str(raw_unit.get("topic") or label).strip()
        focus = str(raw_unit.get("focus") or "").strip()
        if not label:
            continue
        topic = _find_topic(lookup, topic_title, label)
        result = str(topic.get("body") or topic.get("definition") or focus).strip()
        unit: dict[str, Any] = {
            "label": label,
            "display_label": str(raw_unit.get("display_label") or label).strip(),
            "topic": topic_title,
            "focus": focus,
            "result": result or focus,
            "value": str(topic.get("value") or "").strip(),
            "tone": str(topic.get("tone") or "neutral"),
        }
        if topic.get("score") is not None:
            unit["score"] = topic.get("score")
        if topic.get("evidence"):
            unit["evidence"] = str(topic.get("evidence") or "")
        reading_units.append(unit)
    enriched = deepcopy(contract)
    enriched["schema_version"] = PREMIUM_PROFILE_CONTRACT_SCHEMA_VERSION
    enriched["domain"] = normalized
    enriched["reading_units"] = reading_units
    enriched["topic_items"] = topics
    enriched["content_plan"] = premium_content_plan_items(
        normalized,
        content_plan=list(contract.get("content_plan") or []),
        topic_items=topics,
    )
    enriched["content_plan_slots"] = tuple(
        str(item.get("slot") or "").strip()
        for item in enriched["content_plan"]
        if isinstance(item, dict) and str(item.get("slot") or "").strip()
    )
    return enriched


