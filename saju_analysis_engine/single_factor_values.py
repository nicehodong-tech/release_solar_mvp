"""Single-factor base values for elements, stems, and ten-gods.

This module is the third-stage contract for interpretation source values.
The values here are not final customer conclusions. They describe what one
element, one heavenly stem, or one ten-god can contribute before position,
season, strength, protrusion, rooting, branch relations, and paired actions
adjust the judgment.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Iterable

from .constants import ELEMENTS, STEM_METADATA, TEN_GOD_GROUPS
from .output_goals import DOMAIN_OUTPUT_GOAL_ORDER


SINGLE_FACTOR_BASE_VALUE_VERSION = "single_factor_base_value_v1"

SINGLE_FACTOR_CATEGORY_ORDER = ("element", "stem", "ten_god")
SINGLE_FACTOR_DOMAIN_ORDER = DOMAIN_OUTPUT_GOAL_ORDER


@dataclass(frozen=True)
class DomainBaseProjection:
    domain: str
    role: str
    fitting_conditions: tuple[str, ...]
    misfitting_conditions: tuple[str, ...]
    fatigue_or_loss: tuple[str, ...]


@dataclass(frozen=True)
class SingleFactorProfile:
    key: str
    category: str
    label: str
    hanja: str
    material_key: str
    observation_scope: str
    core_property: str
    behavior_property: str
    excess_risk: str
    deficiency_risk: str
    source_sentence_rule: str
    domain_projection: dict[str, DomainBaseProjection]
    direct_conclusion_limits: tuple[str, ...]


def _projection(
    domain: str,
    role: str,
    fitting: tuple[str, ...],
    misfitting: tuple[str, ...],
    fatigue: tuple[str, ...],
) -> DomainBaseProjection:
    return DomainBaseProjection(
        domain=domain,
        role=role,
        fitting_conditions=fitting,
        misfitting_conditions=misfitting,
        fatigue_or_loss=fatigue,
    )


def _profile(
    *,
    key: str,
    category: str,
    label: str,
    hanja: str,
    material_key: str,
    observation_scope: str,
    core_property: str,
    behavior_property: str,
    excess_risk: str,
    deficiency_risk: str,
    source_sentence_rule: str,
    domain_projection: dict[str, DomainBaseProjection],
    direct_conclusion_limits: tuple[str, ...],
) -> SingleFactorProfile:
    return SingleFactorProfile(
        key=key,
        category=category,
        label=label,
        hanja=hanja,
        material_key=material_key,
        observation_scope=observation_scope,
        core_property=core_property,
        behavior_property=behavior_property,
        excess_risk=excess_risk,
        deficiency_risk=deficiency_risk,
        source_sentence_rule=source_sentence_rule,
        domain_projection=domain_projection,
        direct_conclusion_limits=direct_conclusion_limits,
    )


