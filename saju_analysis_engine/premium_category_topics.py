def premium_topic_definition(domain: Any, title: Any) -> str:
    """Return the fixed product meaning for one premium topic.

    The dynamic report body says how the topic appears in this chart. The
    definition says what the topic itself measures, so the UI can read like a
    profile report instead of repeating the same judgment sentence twice.
    """

    normalized = normalize_premium_topic_domain(domain)
    wanted = str(title or "").strip()
    if not wanted:
        return ""
    for item in PREMIUM_CATEGORY_TOPIC_BLUEPRINT.get(normalized) or ():
        if str(item.get("title") or "").strip() == wanted:
            return str(item.get("body") or "").strip()
    return ""


def premium_topic_items(
    domain: Any,
    limit: int = 4,
    *,
    feature_axes: list[dict[str, Any]] | None = None,
) -> list[dict[str, Any]]:
    normalized = normalize_premium_topic_domain(domain)
    items = PREMIUM_CATEGORY_TOPIC_BLUEPRINT.get(normalized) or ()
    axis_keys = PREMIUM_TOPIC_AXIS_KEYS.get(normalized) or ()
    lookup = _axis_lookup(feature_axes)
    result: list[dict[str, Any]] = []
    for index, item in enumerate(items[:limit]):
        keys = axis_keys[index] if index < len(axis_keys) else ()
        result.append(_dynamic_topic_item(dict(item), _matching_axes(lookup, keys)))
    return result
