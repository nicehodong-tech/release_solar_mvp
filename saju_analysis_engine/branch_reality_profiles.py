def normalize_branch_key(branch: str) -> str:
    """Accept a romanized branch key or a Hanja label and return the engine key."""

    cleaned = str(branch or "").strip()
    return BRANCH_LABEL_TO_KEY.get(cleaned, cleaned)


def branch_position_texture(branch: str, position: str) -> str:
    key = normalize_branch_key(branch)
    return BRANCH_POSITION_TEXTURES.get(key, {}).get(position, "")


def branch_domain_texture(branch: str, domain: Domain) -> str:
    key = normalize_branch_key(branch)
    return BRANCH_DOMAIN_TEXTURES.get(key, {}).get(domain, "")
