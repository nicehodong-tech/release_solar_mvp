def premium_detail_entries_for_domain(domain: str) -> tuple[PremiumDetailEntry, ...]:
    """Return premium detail entries for a product domain."""

    return tuple(
        entry for entry in PREMIUM_DETAIL_DICTIONARY.values() if entry.domain == domain
    )


def premium_detail_entry(key: str) -> PremiumDetailEntry | None:
    """Return one premium detail entry by key."""

    return PREMIUM_DETAIL_DICTIONARY.get(key)


def audit_premium_detail_dictionary() -> list[str]:
    """Return human-readable quality issues for the premium detail dictionary."""

    issues: list[str] = []
    seen_keys: set[str] = set()
    for key, entry in PREMIUM_DETAIL_DICTIONARY.items():
        if key in seen_keys:
            issues.append(f"duplicate key: {key}")
        seen_keys.add(key)
        if key != entry.key:
            issues.append(f"{key}: entry.key mismatch")
        if not entry.strong_judgment.endswith("니다."):
            issues.append(f"{key}: strong_judgment must be a decisive polite sentence")
        if len(entry.event_scenes) < 3:
            issues.append(f"{key}: event_scenes must contain at least 3 concrete scenes")
        if len(entry.engine_signal_hints) < 3:
            issues.append(f"{key}: engine_signal_hints must contain at least 3 signals")
        customer_text = " ".join(
            (
                entry.title,
                entry.strong_judgment,
                entry.moderate_judgment,
                entry.weak_judgment,
                " ".join(entry.event_scenes),
                " ".join(entry.premium_notes),
            )
        )
        for fragment in LOW_QUALITY_CUSTOMER_FRAGMENTS:
            if fragment in customer_text:
                issues.append(f"{key}: low-quality fragment detected: {fragment}")
    return issues
