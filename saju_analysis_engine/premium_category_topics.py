def _result_sentence(title: str, score: int | None) -> str:
    choices = TOPIC_RESULT_SENTENCES.get(title) or TOPIC_RESULT_SENTENCES.get(
        TOPIC_RESULT_TITLE_ALIASES.get(title, "")
    )
    if not choices or score is None:
        return ""
    if score >= 90:
        return choices[3]
    if score >= 75:
        return choices[2]
    if score >= 55:
        return choices[1]
    return choices[0]


def _axis_lookup(feature_axes: list[dict[str, Any]] | None) -> dict[str, dict[str, Any]]:
    lookup: dict[str, dict[str, Any]] = {}
    for axis in feature_axes or []:
        if not isinstance(axis, dict):
            continue
        key = str(axis.get("key") or "").strip()
        if key:
            lookup[key] = axis
    return lookup


def _matching_axes(
    lookup: dict[str, dict[str, Any]],
    keys: tuple[str, ...],
) -> list[dict[str, Any]]:
    return [lookup[key] for key in keys if key in lookup]


def _average_score(axes: list[dict[str, Any]]) -> int | None:
    scores = [score for score in (_axis_score(axis) for axis in axes) if score is not None]
    if not scores:
        return None
    return round(sum(scores) / len(scores))


def _evidence_sentence(axes: list[dict[str, Any]], score: int | None) -> str:
    if not axes or score is None:
        return ""
    labels = [str(axis.get("label") or "").strip() for axis in axes if str(axis.get("label") or "").strip()]
    labels = list(dict.fromkeys(labels))[:2]
    if not labels:
        return ""
    return f"분석 기준: {', '.join(labels)} · 결과 등급: {_topic_grade(score)}"


def _dynamic_topic_item(
    item: dict[str, str],
    axes: list[dict[str, Any]],
) -> dict[str, Any]:
    title = str(item.get("title") or "")
    score = _average_score(axes)
    result = _result_sentence(title, score)
    body = result or str(item.get("body") or "")
    payload: dict[str, Any] = {
        "title": title,
        "body": body,
        "definition": str(item.get("body") or ""),
        "tone": _topic_tone(score),
    }
    if score is not None:
        payload["score"] = score
        payload["value"] = _topic_grade(score)
    evidence = _evidence_sentence(axes, score)
    if evidence:
        payload["evidence"] = evidence
    return payload


