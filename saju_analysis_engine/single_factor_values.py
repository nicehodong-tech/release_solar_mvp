SINGLE_FACTOR_BASE_PROFILES: dict[str, SingleFactorProfile] = {
    **ELEMENT_BASE_PROFILES,
    **STEM_BASE_PROFILES,
    **TEN_GOD_BASE_PROFILES,
}


def single_factor_profile_for(key: str) -> SingleFactorProfile:
    try:
        return SINGLE_FACTOR_BASE_PROFILES[key]
    except KeyError as exc:
        raise ValueError(f"Unsupported single factor key: {key}") from exc


def single_factor_domain_projection(key: str, domain: str) -> DomainBaseProjection:
    profile = single_factor_profile_for(key)
    try:
        return profile.domain_projection[domain]
    except KeyError as exc:
        raise ValueError(f"Unsupported single factor domain: {domain}") from exc


def single_factor_profiles_by_category(category: str) -> tuple[SingleFactorProfile, ...]:
    if category not in SINGLE_FACTOR_CATEGORY_ORDER:
        raise ValueError(f"Unsupported single factor category: {category}")
    return tuple(profile for profile in SINGLE_FACTOR_BASE_PROFILES.values() if profile.category == category)


def iter_single_factor_profiles() -> Iterable[SingleFactorProfile]:
    for category in SINGLE_FACTOR_CATEGORY_ORDER:
        yield from single_factor_profiles_by_category(category)


def validate_single_factor_static_contract() -> list[str]:
    errors: list[str] = []
    if tuple(ELEMENT_BASE_PROFILES) != tuple(ELEMENTS):
        errors.append("element base profile order does not match engine element order")
    if set(STEM_BASE_PROFILES) != set(STEM_METADATA):
        errors.append("stem base profile keys do not match engine stem metadata")
    if set(TEN_GOD_BASE_PROFILES) != set(TEN_GOD_GROUPS):
        errors.append("ten-god base profile keys do not match engine ten-god groups")
    for profile in iter_single_factor_profiles():
        if set(profile.domain_projection) != set(SINGLE_FACTOR_DOMAIN_ORDER):
            errors.append(f"{profile.key}: domain projection coverage mismatch")
        if profile.category not in SINGLE_FACTOR_CATEGORY_ORDER:
            errors.append(f"{profile.key}: unsupported category {profile.category}")
        if not profile.direct_conclusion_limits:
            errors.append(f"{profile.key}: missing direct conclusion limits")
    return errors
